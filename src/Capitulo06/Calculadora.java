package Capitulo06;

public class Calculadora {

	public int somar(int n1, int n2) {
		int result = n1 + n2;
		return result;
	}

	public int subtrair(int n1, int n2) {
		int result = n1 - n2;
		return result;
	}

	public double subtrair(double n1, double n2) {
		double result = n1 - n2;
		return result;
	}

	public double subtrair(double n1, int n2) {
		double result = n1 - n2;
		return result;
	}

	public double subtrair(int n1, double n2) {
		double result = n1 - n2;
		return result;
	}

	public int multiplicar(int n1, int n2) {
		int result = n1 * n2;
		return result;
	}

	public int dividir(int n1, int n2) {
		int result = n1 / n2;
		return result;
	}
}
